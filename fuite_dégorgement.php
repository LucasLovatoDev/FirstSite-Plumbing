            <div class="bain-details">
                <div class="bain-heading">
                    <h1>Fuite/Dégorgement</h1>
                </div>
                    <p>
                    <h3>Nos interventions :</h3><br>
                        Recherche de fuites<br>
                        Diagnostic humidité<br>
                        Audit technique bâtiment<br>
                        Traçage et contrôle des réseaux (eau chaude, eau froide, chauffage)<br>
                        Test d’étanchéité des terrasses, façades…<br><br>
                    <h3>Nos techniques :</h3><br>
                        Des interventions non destructives donc sans démolition<br>
                        Des diagnostics précis qui permettent des réparations sur un périmètre clairement identifié<br>
                        Des rapports détaillés incluant photos, commentaires, conclusions et recommandations<br><br>
                    <h3>Nos solutions :</h3><br>
                        <strong>Prise de mesure d’humidité :</strong> l’hygromètre mesure la résistance électrique des matériaux, qui varie en fonction de l’humidité contenue. Le poids d’eau dans le matériau est généralement exprimé en % de la masse.<br><br>

                        <strong>Test de pression :</strong> après avoir raccorder nos manomètres sur un robinet, nous fermons l’arrivée d’eau. Nous laissons le réseau sous pression avant de contrôler le résultat.<br><br>

                        <strong>Endoscopie :</strong> grâce à la fibre optique et à une sonde vidéo miniature, nous inspectons l’état des réseaux. La visualisation se fait grâce à un moniteur couleur intégré.<br><br>

                        <strong>Caméra infrarouge :</strong> nous visualisons les variations de température des différents matériaux grâce à la thermographie et à l’analyse des données.<br><br>

                        <strong>Gaz traceur :</strong> le réseau est vidé et mis sous pression après que le gaz traceur – capable de passer à travers les matériaux même les plus denses –  ait été injecté. Un détecteur contrôle toute trace de gaz en surface.<br><br>

                        <strong>Système électroacoustique :</strong> un bruit se créé lors d’une fuite sous des conduites sous pression. Nous le détectons à l’aide d’un micro de contact.<br><br>

                        <strong>Inspection télévisée :</strong> nous introduisons une sonde vidéo numérique dans la canalisation. Grâce à l’écran couleur, nous en visualisons l’intérieur et  le compte-tours intégré nous permet de localiser l’emplacement des anomalies.<br><br>

                        <strong>Fumigène :</strong> après avoir fait un percement dans l’étanchéité, nous injectons le fumigène sous pression à l’aide d’un surpresseur. La fumée s’échappe alors à travers les défauts d’étanchéité.<br><br>

                        <strong>Colorimétrie :</strong> nous faisons une mise en eau colorée de capacités et/ou canalisations gravitaires soupçonnées fuyardes (bac à douche, bac de rétention, conduites d’évacuation EU, EP, EV…).<br><br>

                        <strong>Champ potentiel (“Leopoma”) :</strong> après avoir effectué une mise en eau, nous divisons notre surface à étudier en 2 moitiés et positionnons des sondes en leur milieu. Nous mesurons les résistances électriques de la membrane d’étanchéité sur les 2 zones : celle présentant la résistance la plus faible contient le(s) point(s) où l’eau s’infiltre.<br><br>

                        <strong>Mise en eau :</strong> arrosage ou simulation de pluies tombantes.<br><br>

                        <strong>Système électromagnétique :</strong> un émetteur envoie un champ électrique  à une fréquence donnée. Cette fréquence est ensuite détectée par notre récepteur grâce à la conductivité des conduites.<br>
